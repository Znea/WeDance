    <h1>Plan de Evacuación en Caso de Incendio para la Academia We Dance</h1>

    <h2>1. Introducción</h2>
    <p>
        La seguridad de todos los estudiantes, instructores y personal administrativo en We Dance es nuestra máxima prioridad. Este plan de evacuación detalla las acciones a seguir en caso de un incendio para garantizar una evacuación ordenada y segura.
    </p>

    <h2>2. Prevención</h2>
    <p>
        -<u>Revisión de equipos:</u> Asegurarse de que los detectores de humo y extintores estén en buen estado y accesibles.<br>
        -<u>Formación regular:</u> Realizar simulacros de incendio y capacitación para todos los ocupantes del edificio.<br>
        -<u>Inspecciones periódicas:</u> Llevar a cabo inspecciones regulares de las instalaciones eléctricas y de gas.
    </p>

    <h2>3. Alarma y Notificación</h2>
    <p>
        -<u>Activación de la alarma:</u> Al detectar un incendio, active inmediatamente la alarma de incendio.<br>
        -<u>Llamada de emergencia:</u> Llamar al número de emergencia 911 y proporcionar detalles del incendio, incluyendo la ubicación y el tamaño del fuego.
    </p>

    <h2>4. Procedimiento de Evacuación</h2>
    <p>
        <strong>a. Instructores y Personal Administrativo</strong><br>
        -<u>Liderar y guiar:</u> Los instructores deben guiar a sus estudiantes hacia las salidas de emergencia más cercanas.<br>
        -<u>Revisión de áreas:</u> Asegurarse de que todas las aulas, baños y áreas comunes estén vacías.<br>
        -<u>Llevar el registro de asistencia:</u> Los instructores deben llevar consigo el registro de asistencia para asegurarse de que todos los estudiantes estén presentes en el punto de reunión.<br><br>
        <strong>b. Estudiantes y Visitantes</strong><br>
        -<u>Mantener la calma:</u> No entrar en pánico y seguir las instrucciones de los instructores.<br>
        -<u>No usar ascensores:</u> Utilizar siempre las escaleras, nunca el ascensor.<br>
        -<u>Salidas de emergencia:</u> Seguir las señales hacia las salidas de emergencia y dirigirse al punto de reunión designado.
    </p>

    <h2>5. Puntos de Reunión</h2>
    <p>
        -<u>Primario:</u> El estacionamiento principal de la academia.<br>
        -<u>Secundario:</u> El parque cercano a 200 metros de la academia.
    </p>

    <h2>6. Asistencia y Control</h2>
    <p>
        -<u>Verificación de asistencia:</u> En el punto de reunión, los instructores deben pasar lista para asegurarse de que todos los estudiantes están presentes.<br>
    </p>
